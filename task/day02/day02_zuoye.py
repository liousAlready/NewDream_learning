'''
dic = {"k1": "v1", "k2": "v2", "k3": "v3"}
# 5.1 输出字典中所有的key
print(dic.keys())

# 5.2 输出字典中所有的value
print(dic.values())

# 5.3  添加一个键值对"k4","v4",输出添加后的字典 dic
dic1 = {'k4': 'v4'}
dic.update(dic1)  # update更新dic1列表至dic列表中
print(dic)

# 5.4  删除字典 dic 中的键值对"k1","v1",并输出删除后的字典 dic
dic.pop('k1')  # 删除列表中的元素
print(dic)

# 5.5 删除字典 dic 中 'k5' 对应的值，若不存在，使其不报错，并返回None
print('删除字典3中x', dic.pop('k5', '不存在该值--None'))

# 5.6 获取字典 dic 中“k2”对应的值
print(dic.get('k2'))  # get方法获取指定key的value值

#  5.7 有字典 dic2 = {'k1':"v111",'a':"b"} 通过一行操作使 dic2 = {'k1':"v111",'k2':"v2",'k3':"v3",'k4': 'v4','a':"b"}
dic1 = {"k2": "v2", "k3": "v3", "k4": "v3"}
dic2 = {'k1': "v111", 'a': "b"}
dic2.update(dic1)
print(dic2)

'''
