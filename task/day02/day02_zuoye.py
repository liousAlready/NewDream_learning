print(lists[0][1][2]['k1'][0]) # 找出'k1'字典中的value
lists[0][1][2]['k1'][0] = lists[0][1][2]['k1'][0].upper()  # 重新赋值给lists TT
print(lists)
# 6.2  获取列表中的 k1对应的值
print("K1对应的值：", lists[0][1][2].values())
